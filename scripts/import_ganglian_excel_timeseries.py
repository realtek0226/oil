@dataclass(frozen=True)
class SeriesColumn:
    sheet_name: str
    column_index: int
    raw_indicator_name: str
    indicator_code: str
    indicator_name: str
    unit: str
    source_name: str
    source_indicator_code: str
    frequency: str
    date_range: str
    description: str
    end_time: str
    update_time: str
    entity_code: str
    entity_name: str
    region_level: str
    product_family: str
    is_core_mapped: bool


def clean_text(value: Any) -> str:
    if value is None:
        return ""
    try:
        if pd.isna(value):
            return ""
    except Exception:
        pass
    return str(value).strip()


def parse_date(value: Any) -> date | None:
    if isinstance(value, datetime):
        return value.date()
    if isinstance(value, date):
        return value
    raw = clean_text(value)
    if not raw:
        return None
    raw = raw.replace("/", "-").replace("～", "-")
    try:
        parsed = pd.Timestamp(raw[:10])
    except Exception:
        return None
    if pd.isna(parsed):
        return None
    return parsed.date()


def parse_number(value: Any) -> float | None:
    if value is None:
        return None
    try:
        if pd.isna(value):
            return None
    except Exception:
        pass
    if isinstance(value, str):
        raw = value.strip().replace(",", "")
        if not raw or raw in {"-", "--", "—", "NaN", "nan", "None"}:
            return None
        if raw.endswith("%"):
            raw = raw[:-1]
        try:
            numeric = float(raw)
        except ValueError:
            return None
    else:
        try:
            numeric = float(value)
        except Exception:
            return None
    return numeric if math.isfinite(numeric) else None


def payload_hash(payload: dict[str, Any]) -> str:
    body = json.dumps(payload, ensure_ascii=False, sort_keys=True, default=str).encode("utf-8")
    return sha256(body).hexdigest()


def source_record_id(source_code: str, payload: dict[str, Any]) -> str:
    digest = payload_hash(payload)[:24]
    return f"{source_code}:{digest}"


def find_meta_rows(raw: pd.DataFrame) -> dict[str, int]:
    rows: dict[str, int] = {}
    if raw.empty:
        return rows
    for row_idx in range(raw.shape[0]):
        row_values = {clean_text(raw.iat[row_idx, col_idx]) for col_idx in range(raw.shape[1])}
        for key, label in META_LABELS.items():
            if key not in rows and label in row_values:
                rows[key] = row_idx
        if len(rows) == len(META_LABELS):
            break
    return rows


def normalize_frequency(value: str, indicator_name: str) -> str:
    text = f"{value} {indicator_name}"
    if "日" in text:
        return "daily"
    if "周" in text:
        return "weekly"
    if "月" in text:
        return "monthly"
    if "季" in text:
        return "quarterly"
    if "年" in text:
        return "yearly"
    return "unknown"


def fallback_indicator_code(source_indicator_code: str, indicator_name: str) -> str:
    if source_indicator_code:
        safe_code = "".join(ch.lower() if ch.isalnum() else "_" for ch in source_indicator_code)
        safe_code = "_".join(part for part in safe_code.split("_") if part)
        if safe_code:
            return f"ganglian_{safe_code}"[:64]
    digest = sha256(indicator_name.encode("utf-8")).hexdigest()[:16]
    return f"ganglian_{digest}"


def infer_entity(indicator_name: str) -> tuple[str, str, str]:
    region_map = [
        ("山东", "SHANDONG", "山东", "province"),
        ("中国", "NATIONAL", "全国", "country"),
        ("华东地区", "EAST_CHINA", "华东", "macro_region"),
        ("华北地区", "NORTH_CHINA", "华北", "macro_region"),
        ("华南地区", "SOUTH_CHINA", "华南", "macro_region"),
        ("华中地区", "CENTRAL_CHINA", "华中", "macro_region"),
        ("西北地区", "NORTHWEST", "西北", "macro_region"),
        ("西南地区", "SOUTHWEST", "西南", "macro_region"),
        ("东北地区", "NORTHEAST", "东北", "macro_region"),
    ]
    for token, code, name, level in region_map:
        if token in indicator_name:
            return code, name, level
    return "NATIONAL", "全国", "country"


def infer_product_family(indicator_name: str) -> str:
    if "柴油" in indicator_name:
        return "DIESEL"
    if "汽油" in indicator_name:
        return "GASOLINE_92" if "92#" in indicator_name else "GASOLINE"
    if "原油" in indicator_name:
        return "CRUDE"
    return "REFINED_OIL"


def build_series_columns(raw: pd.DataFrame, sheet_name: str) -> tuple[list[SeriesColumn], int]:
    meta_rows = find_meta_rows(raw)
    indicator_row = meta_rows.get("indicator_name")
    if indicator_row is None and raw.shape[0] >= 10:
        first_label = clean_text(raw.iat[0, 0]) if raw.shape[1] else ""
        row2_has_values = any(clean_text(raw.iat[1, col_idx]) for col_idx in range(1, raw.shape[1]))
        row11_has_date = parse_date(raw.iat[10, 0]) is not None
        if first_label == "钢联数据" and row2_has_values and row11_has_date:
            meta_rows = {
                "indicator_name": 1,
                "unit": 2,
                "source_name": 3,
                "source_indicator_code": 4,
                "frequency": 5,
                "date_range": 6,
                "description": 7,
                "end_time": 8,
                "update_time": 9,
            }
            indicator_row = 1
    if indicator_row is None:
        return [], 0
    data_start = max(meta_rows.values()) + 1
    columns: list[SeriesColumn] = []
    for col_idx in range(1, raw.shape[1]):
        raw_indicator_name = clean_text(raw.iat[indicator_row, col_idx])
        if not raw_indicator_name:
            continue
        unit = clean_text(raw.iat[meta_rows["unit"], col_idx]) if "unit" in meta_rows else ""
        source_name = clean_text(raw.iat[meta_rows["source_name"], col_idx]) if "source_name" in meta_rows else ""
        source_indicator_code = (
            clean_text(raw.iat[meta_rows["source_indicator_code"], col_idx])
            if "source_indicator_code" in meta_rows
            else ""
        )
        frequency_hint = clean_text(raw.iat[meta_rows["frequency"], col_idx]) if "frequency" in meta_rows else ""
        date_range = clean_text(raw.iat[meta_rows["date_range"], col_idx]) if "date_range" in meta_rows else ""
        description = clean_text(raw.iat[meta_rows["description"], col_idx]) if "description" in meta_rows else ""
        end_time = clean_text(raw.iat[meta_rows["end_time"], col_idx]) if "end_time" in meta_rows else ""
        update_time = clean_text(raw.iat[meta_rows["update_time"], col_idx]) if "update_time" in meta_rows else ""
        mapped = CORE_INDICATOR_BY_NAME.get(raw_indicator_name) or CORE_INDICATOR_BY_SOURCE_CODE.get(source_indicator_code)
        if mapped:
            indicator_code, indicator_name, entity_code, entity_name, region_level, product_family = mapped
            is_core_mapped = True
        else:
            indicator_code = fallback_indicator_code(source_indicator_code, raw_indicator_name)
            indicator_name = raw_indicator_name
            entity_code, entity_name, region_level = infer_entity(raw_indicator_name)
            product_family = infer_product_family(raw_indicator_name)
            is_core_mapped = False
        columns.append(
            SeriesColumn(
                sheet_name=sheet_name,
                column_index=col_idx,
                raw_indicator_name=raw_indicator_name,
                indicator_code=indicator_code,
                indicator_name=indicator_name,
                unit=unit,
                source_name=source_name,
                source_indicator_code=source_indicator_code,
                frequency=normalize_frequency(frequency_hint, raw_indicator_name),
                date_range=date_range,
                description=description,
                end_time=end_time,
                update_time=update_time,
                entity_code=entity_code,
                entity_name=entity_name,
                region_level=region_level,
                product_family=product_family,
                is_core_mapped=is_core_mapped,
            )
        )
    return columns, data_start


def extract_excel_timeseries(
    path: Path,
    *,
    source_code: str,
    start_date: date | None = None,
    end_date: date | None = None,
    mapped_only: bool = True,
    skip_sheet_names: set[str] | tuple[str, ...] = (),
) -> tuple[list[dict[str, Any]], list[dict[str, Any]], dict[str, Any]]:
    if not path.exists():
        raise FileNotFoundError(f"Excel file not found: {path}")
    excel = pd.ExcelFile(path)
    indicator_rows: dict[str, dict[str, Any]] = {}
    data_rows: list[dict[str, Any]] = []
    sheet_summaries: list[dict[str, Any]] = []
    skipped_sheets: list[str] = []

    skip_sheet_name_set = set(skip_sheet_names or ())

    for sheet_name in excel.sheet_names:
        if sheet_name in skip_sheet_name_set:
            skipped_sheets.append(sheet_name)
            continue
        raw = pd.read_excel(path, sheet_name=sheet_name, header=None)
        if raw.empty:
            skipped_sheets.append(sheet_name)
            continue
        series_columns, data_start = build_series_columns(raw, sheet_name)
        if not series_columns:
            skipped_sheets.append(sheet_name)
            continue
        if mapped_only:
            series_columns = [series for series in series_columns if series.is_core_mapped]
        if skip_sheet_name_set:
            series_columns = [series for series in series_columns if series.indicator_code not in SKIP_PRICE_INDICATOR_CODES]
        if not series_columns:
            skipped_sheets.append(sheet_name)
            continue
        rows_before = len(data_rows)
        for series in series_columns:
            indicator_rows.setdefault(
                series.indicator_code,
                {
                    "indicator_code": series.indicator_code,
                    "indicator_name": series.indicator_name,
                    "category": "refined_oil",
                    "sub_category": "ganglian_excel",
                    "unit": series.unit,
                    "freq": series.frequency,
                    "value_type": "number",
                    "fill_policy_default": "latest_available",
                    "description": (
                        f"钢联Excel导入；原指标名称={series.raw_indicator_name}；"
                        f"原指标编码={series.source_indicator_code}；指标描述={series.description}"
                    ),
                    "entity_type": "market_region",
                    "entity_code": series.entity_code,
                    "entity_name": series.entity_name,
                    "region_level": series.region_level,
                    "product_family": series.product_family,
                },
            )
        for row_idx in range(data_start, raw.shape[0]):
            observation_date = parse_date(raw.iat[row_idx, 0])
            if observation_date is None:
                continue
            if start_date and observation_date < start_date:
                continue
            if end_date and observation_date > end_date:
                continue
            for series in series_columns:
                value_num = parse_number(raw.iat[row_idx, series.column_index])
                if value_num is None:
                    continue
                record_payload = {
                    "file_name": path.name,
                    "sheet_name": sheet_name,
                    "row_number": row_idx + 1,
                    "column_number": series.column_index + 1,
                    "observation_date": observation_date.isoformat(),
                    "indicator_code": series.indicator_code,
                    "raw_indicator_name": series.raw_indicator_name,
                    "source_indicator_code": series.source_indicator_code,
                    "value_num": value_num,
                    "unit": series.unit,
                }
                data_rows.append(
                    {
                        "indicator_code": series.indicator_code,
                        "entity_code": series.entity_code,
                        "observation_date": observation_date,
                        "value_num": value_num,
                        "unit": series.unit,
                        "freq": series.frequency,
                        "source_record_id": source_record_id(source_code, record_payload),
                        "raw_payload": record_payload,
                    }
                )
        sheet_summaries.append(
            {
                "sheet_name": sheet_name,
                "indicator_count": len(series_columns),
                "core_mapped_indicator_count": sum(1 for item in series_columns if item.is_core_mapped),
                "data_start_row": data_start + 1,
                "timeseries_rows": len(data_rows) - rows_before,
            }
        )

    summary = {
        "source_code": source_code,
        "excel_path": str(path),
        "sheet_count": len(excel.sheet_names),
        "skipped_sheets": skipped_sheets,
        "indicator_count": len(indicator_rows),
        "core_mapped_indicator_count": sum(1 for item in indicator_rows.values() if item["indicator_code"] in MAPPED_INDICATOR_CODES),
        "timeseries_row_count": len(data_rows),
        "date_min": min((row["observation_date"] for row in data_rows), default=None),
        "date_max": max((row["observation_date"] for row in data_rows), default=None),
        "sheets": sheet_summaries,
        "mapped_only": mapped_only,
    }
    return list(indicator_rows.values()), data_rows, summary


def purge_source_timeseries(repository: PostgresSnapshotRepository, source_code: str) -> dict[str, int]:
    if not repository.engine:
        return {"fact_market_timeseries": 0, "ods_raw_market": 0}
    with repository.engine.begin() as connection:
        source_id = connection.execute(
            text(f"select source_id from {repository._fqtn('dim_source')} where source_code = :source_code"),
            {"source_code": source_code},
        ).scalar_one_or_none()
        if source_id is None:
            return {"fact_market_timeseries": 0, "ods_raw_market": 0}
        fact_result = connection.execute(
            text(f"delete from {repository._fqtn('fact_market_timeseries')} where source_id = :source_id"),
            {"source_id": source_id},
        )
        raw_result = connection.execute(
            text(f"delete from {repository._fqtn('ods_raw_market')} where source_id = :source_id"),
            {"source_id": source_id},
        )
    return {
        "fact_market_timeseries": int(fact_result.rowcount or 0),
        "ods_raw_market": int(raw_result.rowcount or 0),
    }


def upsert_timeseries(
    repository: PostgresSnapshotRepository,
    *,
    source_code: str,
    indicator_rows: list[dict[str, Any]],
    data_rows: list[dict[str, Any]],
    summary: dict[str, Any],
) -> int:
    if not repository.engine or not data_rows:
        return 0
    now = datetime.now(timezone.utc)
    with repository.engine.begin() as connection:
        source_id = repository._ensure_source_ids(connection, {source_code})[source_code]
        repository._ensure_indicators(connection, indicator_rows)
        repository._ensure_entities(connection, indicator_rows)
        raw_payload = {
            "summary": summary,
            "indicator_codes": sorted({row["indicator_code"] for row in indicator_rows}),
        }
        connection.execute(
            text(
                f"""
                insert into {repository._fqtn('ods_raw_market')} (
                    source_id, source_record_id, topic, source_event_time, payload, payload_hash, dt
                )
                values (
                    :source_id, :source_record_id, :topic, :source_event_time,
                    cast(:payload as jsonb), :payload_hash, :dt
                )
                on conflict (source_id, source_record_id, payload_hash) do nothing
                """
            ),
            {
                "source_id": source_id,
                "source_record_id": source_record_id(source_code, raw_payload),
                "topic": "ganglian_excel_timeseries_import",
                "source_event_time": now,
                "payload": json.dumps(raw_payload, ensure_ascii=False, default=str),
                "payload_hash": payload_hash(raw_payload),
                "dt": date.today(),
            },
        )
        rows = list(
            connection.execute(
                text(
                    f"""
                    select i.indicator_id, i.indicator_code, e.entity_id, e.entity_code
                    from {repository._fqtn('dim_indicator')} i
                    join {repository._fqtn('dim_entity')} e on 1 = 1
                    where i.indicator_code in :indicator_codes
                      and e.entity_code in :entity_codes
                    """
                ).bindparams(bindparam("indicator_codes", expanding=True), bindparam("entity_codes", expanding=True)),
                {
                    "indicator_codes": [row["indicator_code"] for row in indicator_rows],
                    "entity_codes": [row["entity_code"] for row in indicator_rows],
                },
            ).mappings()
        )
        indicator_id_map = {str(row["indicator_code"]): int(row["indicator_id"]) for row in rows}
        entity_id_map = {str(row["entity_code"]): int(row["entity_id"]) for row in rows}
        ts_rows: list[dict[str, Any]] = []
        for row in data_rows:
            indicator_id = indicator_id_map.get(row["indicator_code"])
            entity_id = entity_id_map.get(row["entity_code"])
            if not indicator_id or not entity_id:
                continue
            unit = row["unit"] or ""
            observation_time = datetime.combine(row["observation_date"], time(0, 0))
            ts_rows.append(
                {
                    "indicator_id": indicator_id,
                    "entity_id": entity_id,
                    "observation_time": observation_time,
                    "publish_time": now,
                    "freq": row["freq"],
                    "value_num": row["value_num"],
                    "value_text": None,
                    "unit": unit,
                    "currency": "CNY" if "元" in unit else None,
                    "source_id": source_id,
                    "source_record_id": row["source_record_id"],
                    "is_final": True,
                    "revision_no": 1,
                    "quality_flag": "ok",
                    "effective_from": now,
                    "effective_to": None,
                    "dt": row["observation_date"],
                }
            )
        statement = text(
            f"""
            insert into {repository._fqtn('fact_market_timeseries')} (
                indicator_id, entity_id, observation_time, publish_time, freq, value_num,
                value_text, unit, currency, source_id, source_record_id, is_final,
                revision_no, quality_flag, effective_from, effective_to, dt
            )
            values (
                :indicator_id, :entity_id, :observation_time, :publish_time, :freq, :value_num,
                :value_text, :unit, :currency, :source_id, :source_record_id, :is_final,
                :revision_no, :quality_flag, :effective_from, :effective_to, :dt
            )
            on conflict (indicator_id, entity_id, observation_time, revision_no, source_id)
            do update set
                publish_time = excluded.publish_time,
                value_num = excluded.value_num,
                source_record_id = excluded.source_record_id,
                quality_flag = excluded.quality_flag,
                effective_from = excluded.effective_from,
                dt = excluded.dt
            """
        )
        saved = 0
        chunk_size = 5000
        for start in range(0, len(ts_rows), chunk_size):
            result = connection.execute(statement, ts_rows[start : start + chunk_size])
            saved += int(result.rowcount or 0)
        return saved


def resolve_database_settings(args: argparse.Namespace) -> DatabaseSettings:
    database_url = args.database_url or os.getenv("OIL_RESEARCH_DB_URL")
    schema = args.schema or os.getenv("OIL_RESEARCH_DB_SCHEMA")
    if database_url:
        return DatabaseSettings(url=database_url, schema=schema or "oil_research", echo=False)
    settings = get_settings()
    if schema and schema != settings.database.schema:
        return DatabaseSettings(url=settings.database.url, schema=schema, echo=settings.database.echo)
    return settings.database


def write_summary(path: Path, summary: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(summary, ensure_ascii=False, indent=2, default=str), encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description="Import Ganglian horizontal Excel indicators into PostgreSQL.")
    parser.add_argument("--excel", default=str(os.getenv("GANGLIAN_EXCEL_PATH") or DEFAULT_EXCEL_PATH))
    parser.add_argument("--source-code", default=DEFAULT_SOURCE_CODE)
    parser.add_argument("--database-url", default="")
    parser.add_argument("--schema", default="")
    parser.add_argument("--start-date", default="")
    parser.add_argument("--end-date", default="")
    parser.add_argument("--all-columns", action="store_true")
    parser.add_argument("--include-price-sheets", action="store_true", help="Import gasoline/diesel price sheets as well.")
    parser.add_argument("--replace-source", action="store_true")
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument("--summary-output", default="artifacts/ganglian_excel_import_summary.json")
    args = parser.parse_args()

    excel_path = Path(args.excel).expanduser().resolve()
    start_date = parse_date(args.start_date) if args.start_date else None
    end_date = parse_date(args.end_date) if args.end_date else None
    indicator_rows, data_rows, summary = extract_excel_timeseries(
        excel_path,
        source_code=args.source_code,
        start_date=start_date,
        end_date=end_date,
        mapped_only=not args.all_columns,
        skip_sheet_names=() if args.include_price_sheets else SKIP_PRICE_SHEET_NAMES,
    )
    summary["dry_run"] = bool(args.dry_run)
    summary["start_date_filter"] = start_date
    summary["end_date_filter"] = end_date
    summary["mapped_indicator_codes"] = sorted(
        row["indicator_code"]
        for row in indicator_rows
        if row["indicator_code"] in MAPPED_INDICATOR_CODES
    )
    summary["unmapped_indicators"] = [
        {
            "indicator_code": row["indicator_code"],
            "indicator_name": row["indicator_name"],
            "unit": row["unit"],
        }
        for row in indicator_rows
        if row["indicator_code"] not in MAPPED_INDICATOR_CODES
    ]

    if not args.dry_run:
        database_settings = resolve_database_settings(args)
        repository = PostgresSnapshotRepository(database_settings)
        repository.ensure_schema()
        if args.replace_source:
            summary["purged_rows"] = purge_source_timeseries(repository, args.source_code)
        summary["timeseries_saved"] = upsert_timeseries(
            repository,
            source_code=args.source_code,
            indicator_rows=indicator_rows,
            data_rows=data_rows,
            summary=summary,
        )
    else:
        summary["timeseries_saved"] = 0

    output_path = (ROOT / args.summary_output).resolve()
    write_summary(output_path, summary)
    print(json.dumps(summary, ensure_ascii=False, indent=2, default=str))


if __name__ == "__main__":
    main()
