    class PostgresSnapshotRepository:
        def __init__(self, settings: DatabaseSettings) -> None:
            self.settings = settings
            self.engine = create_engine(settings.url, echo=settings.echo) if settings.url else None

        def _fqtn(self, table_name: str) -> str:
            return f"{self.settings.schema}.{table_name}"

        def ensure_schema(self, schema_sql_path: str = "sql/v1_schema.sql") -> None:
            if not self.engine:
                return
            with self.engine.begin() as connection:
                connection.execute(text(f"create schema if not exists {self.settings.schema}"))
                connection.execute(text(f"""
                    create table if not exists {self._fqtn('dim_indicator')} (
                        indicator_id bigint generated by default as identity primary key,
                        indicator_code varchar(64) not null unique,
                        indicator_name varchar(128) not null,
                        category varchar(32) not null,
                        sub_category varchar(32),
                        unit varchar(32) not null,
                        freq varchar(16) not null,
                        value_type varchar(16) not null,
                        publish_lag_rule varchar(128),
                        fill_policy_default varchar(32) not null,
                        description text,
                        is_active boolean not null default true,
                        created_at timestamptz not null default now(),
                        updated_at timestamptz not null default now()
                    )
                """))
                connection.execute(text(f"""
                    create table if not exists {self._fqtn('dim_entity')} (
                        entity_id bigint generated by default as identity primary key,
                        entity_type varchar(32) not null,
                        entity_code varchar(64) not null unique,
                        entity_name varchar(128) not null,
                        parent_entity_id bigint references {self._fqtn('dim_entity')}(entity_id),
                        region_level varchar(16),
                        product_family varchar(32),
                        is_active boolean not null default true,
                        created_at timestamptz not null default now(),
                        updated_at timestamptz not null default now()
                    )
                """))
                connection.execute(text(f"""
                    create table if not exists {self._fqtn('dim_source')} (
                        source_id bigint generated by default as identity primary key,
                        source_code varchar(64) not null unique,
                        source_name varchar(128) not null,
                        source_type varchar(32) not null,
                        priority integer not null default 100,
                        sla_level varchar(16),
                        owner_team varchar(64),
                        is_active boolean not null default true,
                        created_at timestamptz not null default now(),
                        updated_at timestamptz not null default now()
                    )
                """))
                connection.execute(text(f"""
                    create table if not exists {self._fqtn('ods_raw_market')} (
                        raw_id bigint generated by default as identity primary key,
                        source_id bigint not null references {self._fqtn('dim_source')}(source_id),
                        source_record_id varchar(128) not null,
                        topic varchar(64) not null,
                        source_event_time timestamptz,
                        ingest_time timestamptz not null default now(),
                        payload jsonb not null,
                        payload_hash varchar(64) not null,
                        dt date not null,
                        unique (source_id, source_record_id, payload_hash)
                    )
                """))
                connection.execute(text(f"""
                    create table if not exists {self._fqtn('fact_market_timeseries')} (
                        ts_id bigint generated by default as identity primary key,
                        indicator_id bigint not null references {self._fqtn('dim_indicator')}(indicator_id),
                        entity_id bigint not null references {self._fqtn('dim_entity')}(entity_id),
                        observation_time timestamptz not null,
                        publish_time timestamptz not null,
                        freq varchar(16) not null,
                        value_num numeric(20, 6),
                        value_text text,
                        unit varchar(32) not null,
                        currency varchar(16),
                        source_id bigint not null references {self._fqtn('dim_source')}(source_id),
                        source_record_id varchar(128) not null,
                        is_final boolean not null default true,
                        revision_no integer not null default 1,
                        quality_flag varchar(16) not null default 'ok',
                        effective_from timestamptz not null,
                        effective_to timestamptz,
                        ingest_time timestamptz not null default now(),
                        dt date not null,
                        constraint chk_fact_market_timeseries_quality_flag
                            check (quality_flag in ('ok', 'missing', 'outlier', 'revised', 'fallback')),
                        unique (indicator_id, entity_id, observation_time, revision_no, source_id)
                    )
                """))
                connection.execute(text(f"create index if not exists idx_ods_raw_market_dt on {self._fqtn('ods_raw_market')} (dt)"))
                connection.execute(text(f"create index if not exists idx_fact_market_dt on {self._fqtn('fact_market_timeseries')} (dt)"))
                connection.execute(text(f"create index if not exists idx_fact_market_source_dt on {self._fqtn('fact_market_timeseries')} (source_id, dt desc)"))

        def _ensure_source_ids(self, connection: Any, source_codes: set[str]) -> dict[str, int]:
            normalized_codes = sorted(code for code in source_codes if code)
            if not normalized_codes:
                return {}
            seed_rows = []
            for code in normalized_codes:
                if code == "ganglian_excel_import":
                    meta = {"source_name": "Ganglian Excel import", "source_type": "manual_excel", "priority": 2}
                else:
                    meta = {"source_name": code, "source_type": "custom", "priority": 100}
                seed_rows.append({"source_code": code, **meta})
            connection.execute(text(f"""
                insert into {self._fqtn('dim_source')} (source_code, source_name, source_type, priority)
                values (:source_code, :source_name, :source_type, :priority)
                on conflict (source_code) do update set
                    source_name = excluded.source_name,
                    source_type = excluded.source_type,
                    priority = excluded.priority,
                    updated_at = now()
            """), seed_rows)
            rows = connection.execute(
                text(f"select source_id, source_code from {self._fqtn('dim_source')} where source_code in :source_codes")
                .bindparams(bindparam("source_codes", expanding=True)),
                {"source_codes": normalized_codes},
            ).mappings()
            return {str(row["source_code"]): int(row["source_id"]) for row in rows}

        def _ensure_indicators(self, connection: Any, rows: list[dict[str, Any]]) -> None:
            seed_rows = []
            seen: set[str] = set()
            for row in rows:
                indicator_code = str(row["indicator_code"])
                if indicator_code in seen:
                    continue
                seen.add(indicator_code)
                seed_rows.append({
                    "indicator_code": indicator_code,
                    "indicator_name": row["indicator_name"],
                    "category": row["category"],
                    "sub_category": row["sub_category"],
                    "unit": row["unit"],
                    "freq": row["freq"],
                    "value_type": row["value_type"],
                    "fill_policy_default": row["fill_policy_default"],
                    "description": row["description"],
                })
            if seed_rows:
                connection.execute(text(f"""
                    insert into {self._fqtn('dim_indicator')} (
                        indicator_code, indicator_name, category, sub_category, unit,
                        freq, value_type, fill_policy_default, description
                    )
                    values (
                        :indicator_code, :indicator_name, :category, :sub_category, :unit,
                        :freq, :value_type, :fill_policy_default, :description
                    )
                    on conflict (indicator_code) do update set
                        indicator_name = excluded.indicator_name,
                        category = excluded.category,
                        sub_category = excluded.sub_category,
                        unit = excluded.unit,
                        freq = excluded.freq,
                        value_type = excluded.value_type,
                        fill_policy_default = excluded.fill_policy_default,
                        description = excluded.description,
                        updated_at = now()
                """), seed_rows)

        def _ensure_entities(self, connection: Any, rows: list[dict[str, Any]]) -> None:
            seed_rows = []
            seen: set[str] = set()
            for row in rows:
                entity_code = str(row["entity_code"])
                if entity_code in seen:
                    continue
                seen.add(entity_code)
                seed_rows.append({
                    "entity_type": row["entity_type"],
                    "entity_code": entity_code,
                    "entity_name": row["entity_name"],
                    "region_level": row["region_level"],
                    "product_family": row["product_family"],
                })
            if seed_rows:
                connection.execute(text(f"""
                    insert into {self._fqtn('dim_entity')} (
                        entity_type, entity_code, entity_name, region_level, product_family
                    )
                    values (:entity_type, :entity_code, :entity_name, :region_level, :product_family)
                    on conflict (entity_code) do update set
                        entity_name = excluded.entity_name,
                        region_level = excluded.region_level,
                        product_family = excluded.product_family,
                        updated_at = now()
                """), seed_rows)


